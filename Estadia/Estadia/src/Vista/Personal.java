    private void btnElimMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnElimMouseClicked
        int filasel = tbDatos.getSelectedRow();
        if (filasel == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un Registro de la Tabla");
            return;
        }
//        if (txtId.getText().length() == 0) {
//            JOptionPane.showConfirmDialog(null, "Seleccione un registro de la tabla");
//            return;
//        }
        int i = JOptionPane.showConfirmDialog(this, "Si elimina el personal lo borrara de las acciones asociadas a el Â¿Desea Eliminar?", "Confirmar Eliminacion", JOptionPane.YES_NO_OPTION);
        if (i == 0) {
            if (!txtIdPersonal.getText().equals("")) {
                datPersonal.setIdPersonal(txtIdPersonal.getText());
                c.eliminar_personal(datPersonal);
                try {
                    cargar_tabla_Personal("");
                } catch (SQLException ex) {
                    Logger.getLogger(Personal.class.getName()).log(Level.SEVERE, null, ex);
                }
                limpiar();
                bloquear();
            } else {
                JOptionPane.showMessageDialog(null, "No se elimino el personal.");
            }
        }
    }//GEN-LAST:event_btnElimMouseClicked

    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        if (txtNombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar el Nombre completo ");
            txtNombre.requestFocus();
            return;
        }
//        if (txtArea.getText().length() == 0) {
//            JOptionPane.showMessageDialog(null, "Debes ingresar el Area");
//            txtArea.requestFocus();
//            return;
//        }
        if (txtCURP.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar el CURP");
            txtCURP.requestFocus();
            return;
        }
        

        String idPersonal = txtIdPersonal.getText();
        datPersonal.setNombre(txtNombre.getText());
        datPersonal.setCURP(txtCURP.getText());
        datPersonal.setArea(comboAreas.getItemAt(comboAreas.getSelectedIndex()).getNombre());
        

        if (c.Guardar_personal(accion, datPersonal, idPersonal)) {
            if (accion == "A") {
                JOptionPane.showMessageDialog(null, "Personal Agregado.");
            } else if (accion == "M") {
                JOptionPane.showMessageDialog(null, "Personal Actualizado.");
            }
            limpiar();
            bloquear();
            try {
                cargar_tabla_Personal("");
            } catch (SQLException ex) {
                Logger.getLogger(Personal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSaveMouseClicked

